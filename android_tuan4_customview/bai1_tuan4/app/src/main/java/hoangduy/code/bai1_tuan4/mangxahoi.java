package hoangduy.code.bai1_tuan4;

public class mangxahoi {

    int anh;
    String ten;

    public mangxahoi(int anh, String ten) {
        this.anh = anh;
        this.ten = ten;
    }

    public int getAnh() {
        return anh;
    }

    public String getTen() {
        return ten;
    }

    public void setAnh(int anh) {
        this.anh = anh;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }
}
